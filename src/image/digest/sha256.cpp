/*********************** FUNCTION DEFINITIONS ***********************/
/**
 * @brief 执行SHA256哈希计算的核心变换函数
 * @param ctx SHA256上下文指针
 * @param data 输入数据块(64字节)
 * @details 对单个64字节数据块执行SHA256压缩函数。
 *          实现包括消息扩展和64轮哈希计算。
 *          遵循FIPS 180-4标准中的算法规范。
 */
void sha256_transform(SHA256_CTX *ctx, const BYTE data[])
{
	WORD_32 a, b, c, d, e, f, g, h, i, j, t1, t2, m[64];

	for (i = 0, j = 0; i < 16; ++i, j += 4)
		m[i] = (data[j] << 24) | (data[j + 1] << 16) | (data[j + 2] << 8) | (data[j + 3]);
	for ( ; i < 64; ++i)
		m[i] = SIG1(m[i - 2]) + m[i - 7] + SIG0(m[i - 15]) + m[i - 16];

	a = ctx->state[0];
	b = ctx->state[1];
	c = ctx->state[2];
	d = ctx->state[3];
	e = ctx->state[4];
	f = ctx->state[5];
	g = ctx->state[6];
	h = ctx->state[7];

	for (i = 0; i < 64; ++i) {
		t1 = h + EP1(e) + CH(e,f,g) + k[i] + m[i];
		t2 = EP0(a) + MAJ(a,b,c);
		h = g;
		g = f;
		f = e;
		e = d + t1;
		d = c;
		c = b;
		b = a;
		a = t1 + t2;
	}

	ctx->state[0] += a;
	ctx->state[1] += b;
	ctx->state[2] += c;
	ctx->state[3] += d;
	ctx->state[4] += e;
	ctx->state[5] += f;
	ctx->state[6] += g;
	ctx->state[7] += h;
}

/**
 * @brief 初始化SHA256上下文
 * @param ctx 要初始化的SHA256上下文指针
 * @details 将上下文重置为初始状态，包括:
 *          - 设置初始哈希值(前8个素数平方根的小数部分前32位)
 *          - 重置数据长度计数器和位计数器
 */
void sha256_init(SHA256_CTX *ctx)
{
	ctx->datalen = 0;
	ctx->bitlen = 0;
	ctx->state[0] = 0x6a09e667;
	ctx->state[1] = 0xbb67ae85;
	ctx->state[2] = 0x3c6ef372;
	ctx->state[3] = 0xa54ff53a;
	ctx->state[4] = 0x510e527f;
	ctx->state[5] = 0x9b05688c;
	ctx->state[6] = 0x1f83d9ab;
	ctx->state[7] = 0x5be0cd19;
}

/**
 * @brief 更新SHA256哈希计算状态
 * @param ctx SHA256上下文指针
 * @param data 输入数据字节数组
 * @param len 输入数据长度(字节数)
 * @details 处理输入数据，累积到64字节块后触发sha256_transform。
 *          支持分块处理大数据，维护中间状态。
 */
void sha256_update(SHA256_CTX *ctx, const BYTE data[], size_t len)
{
	WORD_32 i;

	for (i = 0; i < len; ++i) {
		ctx->data[ctx->datalen] = data[i];
		ctx->datalen++;
		if (ctx->datalen == 64) {
			sha256_transform(ctx, ctx->data);
			ctx->bitlen += 512;
			ctx->datalen = 0;
		}
	}
}

/**
 * @brief 完成SHA256哈希计算并输出结果
 * @param ctx SHA256上下文指针
 * @param hash 输出缓冲区(32字节)
 * @details 执行最终填充和长度追加，处理最后一个数据块，
 *          并将结果哈希值转换为大端字节序输出。
 *          遵循FIPS 180-4的填充规范。
 */
void sha256_final(SHA256_CTX *ctx, BYTE hash[])
{
	WORD_32 i;

	i = ctx->datalen;

	// 填充缓冲区中剩余的任何数据。
	if (ctx->datalen < 56) {
		ctx->data[i++] = 0x80;
		while (i < 56)
			ctx->data[i++] = 0x00;
	}
	else {
		ctx->data[i++] = 0x80;
		while (i < 64)
			ctx->data[i++] = 0x00;
		sha256_transform(ctx, ctx->data);
		memset(ctx->data, 0, 56);
	}

	// 将总消息长度（以位为单位）附加到填充并进行转换。
	ctx->bitlen += ctx->datalen * 8;
	ctx->data[63] = ctx->bitlen;
	ctx->data[62] = ctx->bitlen >> 8;
	ctx->data[61] = ctx->bitlen >> 16;
	ctx->data[60] = ctx->bitlen >> 24;
	ctx->data[59] = ctx->bitlen >> 32;
	ctx->data[58] = ctx->bitlen >> 40;
	ctx->data[57] = ctx->bitlen >> 48;
	ctx->data[56] = ctx->bitlen >> 56;
	sha256_transform(ctx, ctx->data);

	// 由于此实现使用小端字节排序，而 SHA 使用大端字节序，
	//将最终状态复制到输出哈希时反转所有字节。
	for (i = 0; i < 4; ++i) {
		hash[i]      = (ctx->state[0] >> (24 - i * 8)) & 0x000000ff;
		hash[i + 4]  = (ctx->state[1] >> (24 - i * 8)) & 0x000000ff;
		hash[i + 8]  = (ctx->state[2] >> (24 - i * 8)) & 0x000000ff;
		hash[i + 12] = (ctx->state[3] >> (24 - i * 8)) & 0x000000ff;
		hash[i + 16] = (ctx->state[4] >> (24 - i * 8)) & 0x000000ff;
		hash[i + 20] = (ctx->state[5] >> (24 - i * 8)) & 0x000000ff;
		hash[i + 24] = (ctx->state[6] >> (24 - i * 8)) & 0x000000ff;
		hash[i + 28] = (ctx->state[7] >> (24 - i * 8)) & 0x000000ff;
	}
}
